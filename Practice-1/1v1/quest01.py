
# Прототип на Питоне
# Скорее всего и в будущем работоспособность алгоритма буду проверять именно так
# Тут почти нет подводных камней и синтаксис невозможно забыть

a = [[1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]]

b = [[1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]]

c = [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

n = len(a)

for i in range(n):
    for j in range(n):
        for k in range(n):
            c[j][i] += a[j][k] * b[k][i]

print(c) # [[30, 36, 42], [66, 81, 96], [102, 126, 150]] - верно